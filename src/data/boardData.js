export function resolveCards(templates, squares) {
  const railroads = squares.filter(s => s.type === 'railroad')
  const lastProp  = [...squares].reverse().find(s => s.type === 'property')
  const goSquare  = squares.find(s => s.type === 'go')
  const keyMap = {
    go:        goSquare?.id     ?? 0,
    railroad1: railroads[0]?.id ?? 0,
    railroad3: (railroads[2] ?? railroads[0])?.id ?? 0,
    lastProp:  lastProp?.id     ?? 0,
  }
  return templates.map(card =>
    card.action === 'advance_to' && card.targetKey
      ? { ...card, target: keyMap[card.targetKey] }
      : { ...card }
  )
}

